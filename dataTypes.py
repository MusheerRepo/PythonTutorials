# Numeric
a = 10         # int
b = 10.5       # float
c = 3 + 4j     # complex
print(type(a), type(b), type(c))

# Sequence
s = "hello"    # string
l = [1, 2, 3]  # list
t = (1, 2, 3)  # tuple
print(type(s), type(l), type(t))

